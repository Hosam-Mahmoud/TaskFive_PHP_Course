<?php

// نكتب عدد المسائل يدويًا
$n = 3;

// نكتب المسائل يدويًا في مصفوفة
$problems = [
    [1, 1, 0],
    [1, 1, 1],
    [1, 0, 0]
];

// نبدأ العد من صفر
$count = 0;

// نستخدم حلقة علشان نمر على كل مسألة
for ($i = 0; $i < $n; $i++) {

    // نعد كام واحد قال 1 في المسألة
    $yes = 0;

    if ($problems[$i][0] == 1) {
        $yes++;
    }

    if ($problems[$i][1] == 1) {
        $yes++;
    }

    if ($problems[$i][2] == 1) {
        $yes++;
    }

    // لو على الأقل 2 قالوا 1، نزود العداد
    if ($yes >= 2) {
        $count++;
    }
}

// نطبع النتيجة
echo "عدد المسائل اللي هيحلوها: " . $count;

?>











