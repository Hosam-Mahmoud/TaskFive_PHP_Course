<?php

// نقرأ عدد المسائل من المستخدم
$n = intval(trim(fgets(STDIN)));  
// trim() = بتشيل أي مسافات أو أسطر فاضية في بداية أو نهاية النص
// fgets(STDIN) = بتقرأ سطر من المستخدم
// intval() = بتحول النص لرقم صحيح

// متغير علشان نعد بيه عدد المسائل اللي هيتحلوا
$count = 0;

// نكرر n مرة (عدد المسائل)
for ($i = 0; $i < $n; $i++) {

    // نقرأ السطر اللي فيه آراء 3 أشخاص (مثلاً: 1 1 0)
    $input = trim(fgets(STDIN));

    // نفصل الأرقام عن بعض ونخزنهم في مصفوفة
    $opinions = explode(" ", $input);
    // explode(" ", $input) = بتقسم السطر إلى 3 أجزاء عند كل مسافة

    // نحسب كام واحد قال 1 (يعني واثق)
    $yes_count = 0;
    if ($opinions[0] == 1) $yes_count++;
    if ($opinions[1] == 1) $yes_count++;
    if ($opinions[2] == 1) $yes_count++;

    // لو 2 أو 3 واثقين → نحسبها
    if ($yes_count >= 2) {
        $count++;
    }
}

// نطبع عدد المسائل اللي هيحلوها
echo $count;

?>





















